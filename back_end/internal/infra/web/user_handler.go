package web

import (
	"back_end/internal/entity/interfaces"
	usecase "back_end/internal/usecase"
	"back_end/internal/usecase/dto"
	"encoding/json"
	"net/http"
)

type WebUserHandler struct {
	UserRepository  interfaces.UserRepositoryInterface
	TokenRepository interfaces.TokenRepositoryInterface
}

func NewUserHandler(userRepository interfaces.UserRepositoryInterface, tokenRepository interfaces.TokenRepositoryInterface) *WebUserHandler {
	return &WebUserHandler{
		UserRepository:  userRepository,
		TokenRepository: tokenRepository,
	}
}

func (userHandler *WebUserHandler) Create(w http.ResponseWriter, r *http.Request) {
	if r.Method != http.MethodPost {

		http.Error(w, "Method not allowed", http.StatusMethodNotAllowed)
		return
	}
	var dto dto.UserInputDTO
	err := json.NewDecoder(r.Body).Decode(&dto)
	if err != nil {
		http.Error(w, err.Error(), http.StatusBadRequest)
		return
	}

	createUser := *usecase.NewUserRepository(userHandler.UserRepository, userHandler.TokenRepository)
	output, err := createUser.Create(dto)

	if err != nil {
		http.Error(w, err.Error(), http.StatusInternalServerError)
		return
	}
	err = json.NewEncoder(w).Encode(output)
	if err != nil {
		http.Error(w, err.Error(), http.StatusInternalServerError)
		return
	}

}

func (userHandler *WebUserHandler) CheckPassword(w http.ResponseWriter, r *http.Request) {
	if r.Method != http.MethodGet {
		http.Error(w, "Method not allowed", http.StatusMethodNotAllowed)
		return
	}

	var dto dto.CheckPasswordInputDTO
	err := json.NewDecoder(r.Body).Decode(&dto)
	if err != nil {
		http.Error(w, err.Error(), http.StatusBadRequest)
		return
	}

	createUser := *usecase.NewUserRepository(userHandler.UserRepository, userHandler.TokenRepository)
	output, err := createUser.CheckPassword(dto)

	if err != nil {
		http.Error(w, err.Error(), http.StatusInternalServerError)
		return
	}
	err = json.NewEncoder(w).Encode(output)
	if err != nil {
		http.Error(w, err.Error(), http.StatusInternalServerError)
		return
	}
}
